export const getLanguageLabel = (locale: Locale, languageId: LanguageId) => guideBookEntries[languageId].label[locale]

export const getInitialLocale = (): Locale => {
  if (typeof window === 'undefined') {
    return 'th'
  }

  const storedValue = window.localStorage.getItem(LOCALE_STORAGE_KEY)
  return storedValue === 'en' || storedValue === 'th' ? storedValue : 'th'
}
